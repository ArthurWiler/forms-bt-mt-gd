/* ============================================================
   Helpers de valor
   ------------------------------------------------------------
   O state guarda a string canônica "código - descrição" (o código NÃO é
   escolhido pelo usuário, mas precisa sair no PDF). Os helpers abaixo
   convertem entre essa string e a descrição exibida no campo.
   ============================================================ */

// Índice descrição→código. Descrições repetem no CNAE (o mesmo texto aparece
// com códigos diferentes); guardamos o primeiro código para o caso de um
// state antigo trazer só a descrição.
const _RAMO_POR_DESC = (() => {
  const m = new Map();
  RAMO_ATIVIDADE.forEach(([cod, des]) => {
    const k = des.toUpperCase();
    if (!m.has(k)) m.set(k, cod);
  });
  return m;
})();

// Valor canônico guardado no state para um par [código, descrição].
const ramoCanonico = (cod, des) => `${cod} - ${des}`;

// Descrição a exibir no formulário (sem o código). Aceita tanto o valor
// canônico quanto texto livre / valores antigos sem código.
function ramoDescricao(valor) {
  const v = String(valor == null ? "" : valor).trim();
  const m = v.match(/^(\d{5,7})\s*-\s*(.+)$/);
  return m ? m[2] : v;
}

// Código do ramo (string vazia quando o valor é texto livre sem correspondência).
function ramoCodigo(valor) {
  const v = String(valor == null ? "" : valor).trim();
  const m = v.match(/^(\d{5,7})\s*-\s*/);
  if (m) return m[1];
  return _RAMO_POR_DESC.get(v.toUpperCase()) || "";
}

// Rótulo do PDF: "código - descrição" quando o código é conhecido; caso
// contrário o texto como foi digitado.
function ramoParaPdf(valor) {
  const v = String(valor == null ? "" : valor).trim();
  if (!v) return "";
  const cod = ramoCodigo(v);
  return cod ? ramoCanonico(cod, ramoDescricao(v)) : v;
}

/* ============================================================
   Autocomplete (filtro por digitação)
   ------------------------------------------------------------
   ramoAtivAttach(input, onChange) transforma um <input type="text"> comum
   num campo com lista filtrada. O usuário vê e digita só a descrição;
   onChange recebe o valor canônico ("código - descrição") quando ele
   escolhe um item da lista, ou o texto cru enquanto digita.
   ============================================================ */

const RAMO_ATIV_MAX_ITENS = 50;

// Normaliza para busca: sem acentos, minúsculo. Assim "acucar" acha "AÇÚCAR".
const _ramoNorm = (s) =>
  String(s == null ? "" : s)
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase();

// Índice de busca montado uma vez (1300+ itens filtrados a cada tecla).
const _RAMO_INDICE = RAMO_ATIVIDADE.map(([cod, des]) => ({
  cod,
  des,
  busca: _ramoNorm(des),
}));

// Busca por termos: todos os pedaços digitados precisam aparecer na
// descrição (ordem livre) — "padaria conf" acha "PADARIA E CONFEITARIA...".
// Também aceita a digitação direta do código.
function ramoAtivFiltrar(termo, limite) {
  const t = _ramoNorm(termo).trim();
  const max = limite || RAMO_ATIV_MAX_ITENS;
  if (!t) return _RAMO_INDICE.slice(0, max);
  if (/^\d+$/.test(t))
    return _RAMO_INDICE.filter((r) => r.cod.startsWith(t)).slice(0, max);
  const partes = t.split(/\s+/);
  const res = [];
  for (const r of _RAMO_INDICE) {
    if (partes.every((p) => r.busca.includes(p))) {
      res.push(r);
      if (res.length >= max) break;
    }
  }
  return res;
}

function ramoAtivAttach(input, onChange) {
  if (!input || input.dataset.ramoAtiv === "1") return input;
  input.dataset.ramoAtiv = "1";
  input.setAttribute("autocomplete", "off");
  input.setAttribute("role", "combobox");
  input.setAttribute("aria-autocomplete", "list");
  input.setAttribute("aria-expanded", "false");

  // A lista é posicionada em relação ao .field que envolve o input; como o
  // .field é criado depois em algumas telas, resolvemos o pai na abertura.
  const lista = document.createElement("div");
  lista.className = "ramo-ativ-lista";
  lista.setAttribute("role", "listbox");
  lista.hidden = true;

  let itens = [];
  let ativo = -1;

  const fechar = () => {
    lista.hidden = true;
    ativo = -1;
    input.setAttribute("aria-expanded", "false");
  };

  const marcar = (i) => {
    const filhos = lista.children;
    if (ativo >= 0 && filhos[ativo]) filhos[ativo].classList.remove("is-ativo");
    ativo = i;
    if (ativo >= 0 && filhos[ativo]) {
      filhos[ativo].classList.add("is-ativo");
      filhos[ativo].scrollIntoView({ block: "nearest" });
    }
  };

  const escolher = (r) => {
    input.value = r.des; // o formulário mostra só a descrição
    fechar();
    if (onChange) onChange(ramoCanonico(r.cod, r.des), r);
  };

  const abrir = () => {
    const pai = input.closest(".field") || input.parentNode;
    if (lista.parentNode !== pai) {
      if (getComputedStyle(pai).position === "static")
        pai.style.position = "relative";
      pai.appendChild(lista);
    }
    itens = ramoAtivFiltrar(input.value);
    if (!itens.length) {
      fechar();
      return;
    }
    lista.innerHTML = itens
      .map(
        (r) =>
          `<div class="ramo-ativ-item" role="option">${r.des.replace(/&/g, "&amp;").replace(/</g, "&lt;")}</div>`,
      )
      .join("");
    Array.from(lista.children).forEach((el, i) => {
      // mousedown (não click): dispara antes do blur fechar a lista.
      el.addEventListener("mousedown", (e) => {
        e.preventDefault();
        escolher(itens[i]);
      });
      el.addEventListener("mouseenter", () => marcar(i));
    });
    lista.hidden = false;
    ativo = -1;
    input.setAttribute("aria-expanded", "true");
  };

  input.addEventListener("input", () => {
    abrir();
    // Texto digitado que não corresponde a um item vale como texto livre.
    if (onChange) onChange(input.value, null);
  });
  input.addEventListener("focus", abrir);
  input.addEventListener("blur", () => setTimeout(fechar, 0));
  input.addEventListener("keydown", (e) => {
    if (lista.hidden && (e.key === "ArrowDown" || e.key === "ArrowUp")) {
      abrir();
      return;
    }
    if (lista.hidden) return;
    if (e.key === "ArrowDown") {
      e.preventDefault();
      marcar(Math.min(ativo + 1, itens.length - 1));
    } else if (e.key === "ArrowUp") {
      e.preventDefault();
      marcar(Math.max(ativo - 1, 0));
    } else if (e.key === "Enter") {
      if (ativo >= 0) {
        e.preventDefault();
        escolher(itens[ativo]);
      }
    } else if (e.key === "Escape") {
      fechar();
    }
  });

  return input;
}
